#!/bin/bash

set -e

# ======================================================================================
# Install ComfyUI
# ======================================================================================
python3 -m venv comfy_env
source ./comfy_env/bin/activate

pip install comfy-cli
comfy --skip-prompt install --m-series

# ======================================================================================
# Start ComfyUI
# ======================================================================================
nohup comfy launch --background -- --port 8080
